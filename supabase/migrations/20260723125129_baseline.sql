CREATE OR REPLACE FUNCTION "public"."sync_game_release_to_catalog"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO ''
    AS $$
begin
  insert into public.game_platforms (
    game_id,
    platform_id,
    region,
    release_date,
    physical,
    digital,
    edition_name
  )
  values (
    new.game_id,
    new.platform_id,
    coalesce(new.region, 'WORLD'),
    new.release_date,
    coalesce(new.physical, false),
    coalesce(new.digital, false),
    new.edition_name
  )
  on conflict (game_id, platform_id, region)
  do update set
    release_date = coalesce(
      excluded.release_date,
      public.game_platforms.release_date
    ),
    physical = public.game_platforms.physical or excluded.physical,
    digital = public.game_platforms.digital or excluded.digital,
    edition_name = coalesce(
      excluded.edition_name,
      public.game_platforms.edition_name
    ),
    updated_at = now();

  return new;
end;
$$;


ALTER FUNCTION "public"."sync_game_release_to_catalog"() OWNER TO "postgres";

